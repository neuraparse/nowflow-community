function normalizeDomain(email: string): string | null {
  const at = email.lastIndexOf('@')
  if (at === -1 || at === email.length - 1) return null
  return email
    .slice(at + 1)
    .trim()
    .toLowerCase()
}

/**
 * Returns true if the email address belongs to a known throwaway provider.
 * Domain match is case-insensitive and exact — subdomains are not matched
 * (mailinator.gq and mailinator.com are separate entries).
 */
export function isDisposableEmail(email: string | null | undefined): boolean {
  if (!email) return false
  const domain = normalizeDomain(email)
  if (!domain) return false
  return DISPOSABLE_DOMAINS.has(domain)
}

export function disposableDomainCount(): number {
  return DISPOSABLE_DOMAINS.size
}
